// C Program to Print Your Own Name using printf
#include <stdio.h>   // Header file for input/output functions

int main() {
  
    // Printing your name "Aishwarya" on the output screen
    // \n is used to move the cursor to the next line after printing
    printf("Aishwarya\n");

    return 0;  // Exit status of the program (0 means successful)
}


